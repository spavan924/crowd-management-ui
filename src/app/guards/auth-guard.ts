import { inject } from '@angular/core';
import { Router } from '@angular/router';
import { AuthService } from '../auth/auth';

export const authGuard = () => {
  const auth = inject(AuthService);
  const router = inject(Router);

  if (auth.isLoggedIn()) {
    return true;
  }

  router.navigate(['/login']);
  return false;
};
